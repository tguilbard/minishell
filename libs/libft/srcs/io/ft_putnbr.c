#include "libft.h"

void  ft_putnbr(int nb, int fd)
{
	if (nb < 0)
	{
		ft_putchar('-', fd);
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10, fd);
	}
	ft_putchar((nb % 10) + '0', fd);
}
